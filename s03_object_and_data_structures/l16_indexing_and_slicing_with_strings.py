my_string = 'abcdefghijk'

print(my_string[2:]) #print from index 2 to end of string

print(my_string[:3]) #print up to but not including index 3

print(my_string[3:6]) #print from index 3 up to but not including index 6

print(my_string[1:3])

print(my_string[::2]) #print from begining to end in step size of 2

print(my_string[::-1]) #print in reverse order

#print from index 2 ('c') up to index 7 ('h') with stepsize of 2
print(my_string[2:7:2]) #combining everything